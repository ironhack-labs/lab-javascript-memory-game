document.addEventListener("DOMContentLoaded", function(event) { 
  let html = '';
  memoryGame.cards.forEach(pic => {
    html += `<div class="card" data-card-name="${pic.name}">`;
    html += `<div class="back" name="${pic.img}"></div>`;
    html += `<div class="front" style="background: url(img/${pic.img}) no-repeat"></div>`;
    html += `</div>`;
  });

  // Add all the divs to the HTML
  document.querySelector('#memory_board').innerHTML = html;

  // Bind the click event of each element to a function
  document.querySelectorAll('.card').forEach( card => {
    card.onclick = function() {
      // TODO: write some code here
      //Flip a card   
      card.classList.add("turned");

      let arrayCards= memoryGame.pickedCards.length;

      memoryGame.pickedCards.push(card);

      if(arrayCards === 1){
       let areSame= memoryGame.checkIfPair(memoryGame.pickedCards[0], memoryGame.pickedCards[1]);
      
        if(areSame){
          memoryGame.addPaisGuessed();
        }else{
          memoryGame.turnCard(areSame,memoryGame.pickedCards[0],memoryGame.pickedCards[1]);
        }
        
        memoryGame.pickedCards=[];
  
      }
      memoryGame.addPaisClicked();
      memoryGame.isFinished();
    };
  });
});


