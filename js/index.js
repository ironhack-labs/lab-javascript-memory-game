const memoryGame = new MemoryGame(cards);

window.addEventListener('load', event => {
  let html = '';
  memoryGame.cards.forEach(pic => {
    html += `<div class="card" data-card-name="${pic.name}">`;
    html += `<div class="back" name="${pic.img}"></div>`;
    html += `<div class="front" style="background: url(img/${pic.img}) no-repeat"></div>`;
    html += `</div>`;
  });

  // Add all the divs to the HTML
  document.querySelector('#memory-board').innerHTML = html;

  // Bind the click event of each element to a function
  let pair = [];

  document.querySelectorAll('.card').forEach(card => {
    card.addEventListener('click', () => {
      if (pair.length < 2) {
        card.classList.toggle('turned');
        pair.push(card);
        if (pair.length === 2) {
          if (!memoryGame.checkIfPair(pair[0].getAttribute('data-card-name'), pair[1].getAttribute('data-card-name'))) {
            setTimeout(() => {
              pair[0].classList.toggle('turned');
              pair[1].classList.toggle('turned');
              pair = [];
            }, 1000);
          } else {
            pair = [];
          }
        }
        document.getElementById("pairs-clicked").innerHTML = memoryGame.pairsClicked;
        document.getElementById("pairs-guessed").innerHTML = memoryGame.pairsGuessed;

        memoryGame.pickedCards.length = 0;
      }
      if (memoryGame.isFinished()) {

        alert("You are a winner, baby!")
      }
    });
  });
});